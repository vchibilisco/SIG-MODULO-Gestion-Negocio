    private void eliminar() {
        if (!listaRubr.getSelectedItem().equals("Seleccione un Rubro")) {
            bandera = CONTROLADOR_V.getContro_m().eliminarRubro(Integer.parseInt(this.txtCodigo.getText()));
            if (bandera == true) {
                cargarTabla();
                JOptionPane.showMessageDialog(this, "Rubro: " + (String) listaRubr.getSelectedItem() + " eliminado", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);
                CONTROLADOR_V.mensaje("Rubro: " + (String) listaRubr.getSelectedItem() + " eliminado");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Rubro: " + (String) listaRubr.getSelectedItem() + " no se pudo eliminar", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);
                CONTROLADOR_V.mensaje("Rubro: " + (String) listaRubr.getSelectedItem() + " no se pudo eliminar");
                limpiar();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor seleccione un Rubro.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void limpiar() {
        listaRubr.setModel(new javax.swing.DefaultComboBoxModel(new String[]{"Seleccione un Rubro"}));
        CONTROLADOR_V.recupNombresRubros(this.listaRubr);
        this.txtCodigo.setText("");
    }

    private void cargarTabla() {
        tabla.setModel(modelo = new ModeloTabla());
        modelo.addColumn("Codigo");
        modelo.addColumn("Descripcion");
        if (!listaRubr.getSelectedItem().equals("Seleccione un Rubro")) {
            String nombre = (String) listaRubr.getSelectedItem();
            modelo = CONTROLADOR_V.getContro_m().recuperarDatos(nombre, modelo, "rubro");
        }
        TableColumn codigo = tabla.getColumn("Codigo");
        codigo.setMaxWidth(70);
        codigo.setMinWidth(70);
        TableColumn descripcion = tabla.getColumn("Descripcion");
        descripcion.setMaxWidth(568);
        descripcion.setMinWidth(568);
    }
