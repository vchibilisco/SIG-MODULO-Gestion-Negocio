    private void eliminar() {
        if (!listaMarca.getSelectedItem().equals("Seleccione un Rubro")) {
            if (CONTROLADOR_V.getContro_m().eliminarMarca(Integer.parseInt(this.txtCodigo.getText()))) {
                cargarTabla();
                JOptionPane.showMessageDialog(this, "Marca: " + (String) this.listaMarca.getSelectedItem() + " eliminado", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);
                CONTROLADOR_V.mensaje("Marca: " + (String) this.listaMarca.getSelectedItem() + " eliminado");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Marca: " + (String) this.listaMarca.getSelectedItem() + " no se pudo eliminar", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);
                CONTROLADOR_V.mensaje("Marca: " + (String) this.listaMarca.getSelectedItem() + " no se pudo eliminar");
                limpiar();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor seleccione un Rubro.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void limpiar() {
        listaMarca.setModel(new javax.swing.DefaultComboBoxModel(new String[]{"Seleccione una Marca"}));
        CONTROLADOR_V.recupNombresMarca(this.listaMarca);
        this.txtCodigo.setText("");
    }

    private void cargarTabla() {
        tabla.setModel(modelo = new ModeloTabla());
        modelo.addColumn("Codigo");
        modelo.addColumn("Descripcion");
        if (!listaMarca.getSelectedItem().equals("Seleccione una Marca")) {
            String nombre = (String) listaMarca.getSelectedItem();
            modelo = CONTROLADOR_V.getContro_m().recuperarDatos(nombre, modelo, "marca");
        }
        TableColumn codigo = tabla.getColumn("Codigo");
        codigo.setMaxWidth(70);
        codigo.setMinWidth(70);
        TableColumn descripcion = tabla.getColumn("Descripcion");
        descripcion.setMaxWidth(568);
        descripcion.setMinWidth(568);
    }
