    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = this.txtNombre.getText().toUpperCase();
        if (!listaMarca.getSelectedItem().equals("Seleccione una Marca")) {
            if (!nombre.equals((String) listaMarca.getSelectedItem()) || !nombre.equals("")) {
                if (txtNombre.getText().length() <= 120) {
                    if (Validacion.validarString(nombre)) {
                        if (CONTROLADOR_V.getContro_m().modificarMarca((String) this.listaMarca.getSelectedItem(), nombre)) {
                            JOptionPane.showMessageDialog(this, "Marca: " + listaMarca.getSelectedItem() + " actualizado a " + nombre, "INFORMACION", JOptionPane.INFORMATION_MESSAGE);
                            CONTROLADOR_V.mensaje("Marca: " + listaMarca.getSelectedItem() + " actualizado a " + nombre);
                            limpiar();
                        } else {
                            JOptionPane.showMessageDialog(this, "Marca: " + nombre + " no se actualizo."
                                    + "             \nVerifique que no exista el Rubro que desea ingresar.", "ERROR", JOptionPane.INFORMATION_MESSAGE);
                            CONTROLADOR_V.mensaje("Marca: " + nombre + " no se actualizo");
                            this.txtNombre.selectAll();
                            this.txtNombre.requestFocus();
                        }
                    } else {
                        txtNombre.requestFocus();
                        txtNombre.selectAll();
                        JOptionPane.showMessageDialog(null, "Este campo solo admite letras. \nPor favor verifique.",
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    txtNombre.requestFocus();
                    txtNombre.selectAll();
                    JOptionPane.showMessageDialog(null, "Este campo no debe superar los 120 caracteres. \nPor favor verifique.",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                txtNombre.requestFocus();
                txtNombre.selectAll();
                JOptionPane.showMessageDialog(null, "Por favor complete los campos faltantes o verifique no estar ingresando"
                        + "                     \nel mismo nombre de la Marca que desea cambiar. "
                        + "                     \nPor favor verifique.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor seleccione una Marca.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

